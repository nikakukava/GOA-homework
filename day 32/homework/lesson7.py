#1. Verify if a word consists only of uppercase letters using the isupper() method before further processing or analysis.

inp = str(input("please enter a string : "))
if inp.isupper() == inp:
    print("the string consists only of uppercase letters")
else:
    print("the string does not consist only of uppercase letters")

